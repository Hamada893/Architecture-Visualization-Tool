import Navbar from "components/Navbar";
import { Button } from "components/ui/Button";
import { ArrowRight, ArrowUpRight, Clock, Layers} from "lucide-react"
import Upload from "components/Upload";
import { useNavigate } from "react-router";
import { useRef, useState, useEffect } from "react"
import { createProject, getProject, getProjects } from "lib/puter.action";

export default function Home() {
  const navigate = useNavigate()
  const [projects, setProjects] = useState<DesignItem[]>([])
  const isCreatingProjectRef = useRef(false)

  const handleUploadComplete = async (base64Image: string) => {
      if (isCreatingProjectRef.current) return false
      isCreatingProjectRef.current = true
      try {
      const newId = Date.now().toString()
      const name = `Residence ${newId}`
      const newItem = {
        id: newId, 
        name, sourceImage: base64Image, 
        renderedImage: undefined,
        timestamp: Date.now()
      }

      const saved = await createProject(({ item: newItem}))
      if(!saved) {
        console.error("Failed to create project")
        return false
      }

      setProjects(((prev) => [saved, ...prev]))

      navigate(`/visualizer/${newId}`, {
        state: {
          initialImage: saved.sourceImage,
          initialRendered: saved.renderedImage || null,
          name
        }
      })

      return true
    } finally {
        isCreatingProjectRef.current = false
      }
    
  }
  
  useEffect(() => {
    const fetchProjects = async () => {
      const items = await getProjects()
      setProjects(items)
    }
    fetchProjects()
  }, [])
  
  return (
    <div className="home">
      <Navbar />

      <section className="hero">
        <div className="announce">
          <div className="dot">
            <div className="pulse"></div>
          </div>
          <span>Visualize Your 2D Architecture Plans</span>
        </div>

        <h1>Build Your Dream Home in 3D with AI Visualization in seconds</h1>

        <p className="subtitle">
        Turn your 2D architecture plans into stunning 3D visualizations in minutes with this AI-powered platform.
        </p>

        <div className="actions">
          <a href="#upload" className="cta">
            Start Building <ArrowRight className="icon" />
          </a>
          <Button variant="outline" size="lg" className="demo">
            Watch Demo
          </Button>
        </div>

        <div id="upload" className="upload-shell">
          <div 
            className="grid-overlay" 
            style={{
              backgroundImage:
                "linear-gradient(#3B82F6 1px, transparent 1px), linear-gradient(90deg, #3B82F6 1px, transparent 1px)",
              backgroundSize: "60px 60px",
            }}
          ></div>
          <div className="upload-card">
            <div className="upload-head">
              <div className="upload-icon">
                  <Layers className="icon" />
              </div>
                <h3>Upload your floor plan</h3>
                <p>Support JPG, PNG formats up to 10MB</p>
              </div>  

              <Upload onComplete={handleUploadComplete}/>
          </div>
        </div>
      </section>

      <section className="projects">
        <div className="section-inner">
          <div className="section-head">
            <div className="copy">
              <h2>Projects</h2>
              <p>Recent work and shared community projects</p>
            </div>
          </div>

          <div className="projects-grid">
            {projects.map(({id, name, renderedImage, sourceImage, timestamp}) => (
              <div key={id} className="project-card group" onClick={() => {navigate(`visualizer/${id}`)}}>
              <div className="preview">
                <img 
                  src={renderedImage || sourceImage}
                  alt="Finished demo project"
                />

                <div className="badge">
                  <span>Community</span>
                </div>
              </div>
              <div className="card-body">
                <div>
                  <h3>{name}</h3>

                  <div className="meta">
                    <Clock size={12}/>
                    <span>{new Date(timestamp).toLocaleDateString()}</span>

                    <span>By John Doe</span>
                  </div>
                </div>
                <div className="arrow">
                  <ArrowUpRight size={18} />
                </div>
              </div>
            </div>
            ))}
          </div>
        </div>
      </section>
    </div>
  )
}
